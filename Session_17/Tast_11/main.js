// Sử dụng hàm Math.abs()
let absValue1 = Math.abs(-5.5);
// Kết quả: 5.5
// Math.abs() trả về giá trị tuyệt đối của số được truyền vào, tức là giá trị không âm.

let absValue2 = Math.abs(10);
// Kết quả: 10
// Math.abs() trả về giá trị tuyệt đối của số được truyền vào, trong trường hợp này số đã là số dương.

// Sử dụng hàm Math.ceil()
let ceilValue1 = Math.ceil(4.4);
// Kết quả: 5
// Math.ceil() làm tròn số lên đến số nguyên gần nhất.

let ceilValue2 = Math.ceil(5.8);
// Kết quả: 6
// Math.ceil() làm tròn số lên đến số nguyên gần nhất.

// Sử dụng hàm Math.floor()
let floorValue1 = Math.floor(5.7);
// Kết quả: 5
// Math.floor() làm tròn số xuống đến số nguyên gần nhất.

let floorValue2 = Math.floor(6.1);
// Kết quả: 6
// Math.floor() làm tròn số xuống đến số nguyên gần nhất.

// Sử dụng hàm Math.round()
let roundValue1 = Math.round(10.1);
// Kết quả: 10
// Math.round() làm tròn số đến số nguyên gần nhất, nếu phần thập phân nhỏ hơn 0.5 thì làm tròn xuống.

let roundValue2 = Math.round(20.6);
// Kết quả: 21
// Math.round() làm tròn số đến số nguyên gần nhất, nếu phần thập phân lớn hơn hoặc bằng 0.5 thì làm tròn lên.