let month = parseInt(prompt("Nhập vào một số tháng : "));
if (month === 1,2,3) {
    conmonthle.log("Mùa Xuân");
} else if(month === 4,5,6) {
    conmonthle.log("Mùa Hạ");
} else if(month === 7,8,9) {
    conmonthle.log("Mùa Thu");
} else if(month === 10,11,12) {
    conmonthle.log("Mùa Đông");
} else {
    conmonthle.log("Tháng không hợp lệ");
}